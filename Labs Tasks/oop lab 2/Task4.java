// Task4. Write java code that define radius for a circle and print the circumference, diameter and area for the circle.
class Task4{
	public static void main(String[] args) {
	float radius_of_circle = 3.53f;
	double circumference = 2 * 3.14159 * radius_of_circle;
	float diameter = 2 * radius_of_circle;

	System.out.println("Radius Of Circle = " + radius_of_circle);
	System.out.println("Circumference = " + circumference);
	System.out.println("Diameter = " + diameter);	
	}
} 